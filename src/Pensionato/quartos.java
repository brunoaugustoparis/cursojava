package Pensionato;

public class quartos {
	
	private String nome;
	private String email;
	private int numQuarto;
	
	public quartos(String nome, String email, int numQuarto) {
		this.nome = nome;
		this.email = email;
		this.numQuarto = numQuarto;
	}

	public String getNome() {
		return nome;
	}

	public String getEmail() {
		return email;
	}

	public int getNumQuarto() {
		return numQuarto;
	}

	
	
	

}
